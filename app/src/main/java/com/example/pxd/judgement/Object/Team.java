public class Team {
    int t_id;
    String t_name;
    int t_cnt;
    String t_leader;
    String t_phone;

    public int getT_id() {
        return t_id;
    }

    public void setT_id(int t_id) {
        this.t_id = t_id;
    }

    public String getT_name() {
        return t_name;
    }

    public void setT_name(String t_name) {
        this.t_name = t_name;
    }

    public int getT_cnt() {
        return t_cnt;
    }

    public void setT_cnt(int t_cnt) {
        this.t_cnt = t_cnt;
    }

    public String getT_leader() {
        return t_leader;
    }

    public void setT_leader(String t_leader) {
        this.t_leader = t_leader;
    }

    public String getT_phone() {
        return t_phone;
    }

    public void setT_phone(String t_phone) {
        this.t_phone = t_phone;
    }
}
